#include "ui.h"
#include "score.h"
#include "time.h"

//=============================================================================
//静的メンバ変数宣言
//=============================================================================
CScore * CUi::pScore = NULL;
CTime * CUi::pTime = NULL;

//=============================================================================
//UIクラスのコンストラクタ
//=============================================================================
CUi::CUi()
{
}

//=============================================================================
//UIクラスのデストラクタ
//=============================================================================
CUi::~CUi()
{
}

//=============================================================================
//UIクラスの初期化処理
//=============================================================================
HRESULT CUi::Init(void)
{
	pScore = CScore::Create();
	pTime = CTime::Create();
	return S_OK;
}

//=============================================================================
//UIクラスの終了処理
//=============================================================================
void CUi::Uninit(void)
{
}

//=============================================================================
//UIクラスの更新処理
//=============================================================================
void CUi::Update(void)
{
	if (pScore)
	{
		// スコアの更新
		pScore->Update();
	}

	if (pTime)
	{
		// 時間の減算
		pTime->SubTime();
	}
}

//=============================================================================
//UIクラスの描画処理
//=============================================================================
void CUi::Draw(void)
{
}

//=============================================================================
//UIクラスのスコア取得処理
//=============================================================================
CScore * CUi::GetScore(void)
{
	return pScore;
}

//=============================================================================
//UIクラスのタイム取得処理
//=============================================================================
CTime * CUi::GetTime(void)
{
	return pTime;
}
