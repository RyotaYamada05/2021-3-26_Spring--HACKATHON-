#include "item.h"

//=============================================================================
//アイテムクラスのコンストラクタ
//=============================================================================
CItem::CItem()
{
}

//=============================================================================
//アイテムクラスのデストラクタ
//=============================================================================
CItem::~CItem()
{
}

//=============================================================================
//アイテムクラスのクリエイト処理
//=============================================================================
CItem * CItem::Create(D3DXVECTOR3 pos, D3DXVECTOR3 size)
{
	CItem *pItem = new CItem;

	if (pItem)
	{
		pItem->SetPos(pos);
		pItem->SetSize(size);
		pItem->Init();
	}
	return pItem;
}

//=============================================================================
//アイテムクラスの初期化処理
//=============================================================================
HRESULT CItem::Init(void)
{
	//CScene2Dの初期化
	CScene2D::Init();
	return E_NOTIMPL;
}

//=============================================================================
//アイテムクラスの終了処理
//=============================================================================
void CItem::Uninit(void)
{
	//CScene2Dの終了
	CScene2D::Uninit();
}

//=============================================================================
//アイテムクラスの更新処理
//=============================================================================
void CItem::Update(void)
{
	//CScene2Dの更新
	CScene2D::Update();
}

//=============================================================================
//アイテムクラスの描画処理
//=============================================================================
void CItem::Draw(void)
{
	//CScene2Dの描画
	CScene2D::Draw();
}
